const textMessage = 'Je suis du texte';

// NUMBER (Tous les types d'écriture numériques sont possibles : positif, negatif, decimal)
const positiveInteger = 89;   // Positive Integer
const negativeInteger = -10;  // Negative Integer
const decimalNumber = 56.8;   // Decimal Number

// BOOLEAN (booléens)
// Variable de type booleen, n'accepte que 2 valeurs TRUE ou FALSE
const isTrue = true
const isFalse = false

const nullValue = null

let undefinedValue; //undefined

console.log(typeof textMessage);
